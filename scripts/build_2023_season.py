    for rnd, home, away, hg, ag in ALL_OTHER_MATCHES:
        if (rnd, home, away) in seen:
            continue
        mid += 1
        date_str = ROUND_DATES.get(rnd, f"2023-01-01")
        matches.append({
            "match_id": str(mid),
            "date": f"{date_str} 19:35:00",
            "home_club": home, "away_club": away,
            "status": "finished",
            "score": {"home": hg, "away": ag},
            "round": f"第{rnd}轮",
            "venue": {"name": "", "city": None},
            "events": [],
            "source": "guoan_dashboard_2023",
        })
        seen.add((rnd, home, away))

    return matches


